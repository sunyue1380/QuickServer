    /**
     * 根据文件后缀名获取对应MIME类型
     * */
    public static String getMIMEType(String name){
        String suffix = name.substring(name.lastIndexOf(".")+1);
        String mimeType = mimetype.get(suffix);
        if(mimeType==null){
            mimeType = "application/octet-stream";
        }
        return mimeType;
    }
